function slugify(input) {
  return String(input || "")
    .trim()
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)+/g, "");
}

function jsonString(v) {
  if (typeof v === "string") return v;
  return JSON.stringify(v ?? {});
}

